package gestionDechet;

/**
 * Enumération des différentes couleurs de poubelles
 */
public enum Couleur {
    VERT,   // Poubelle pour les déchets en verre
    BLEUE,  // Poubelle pour les papiers
    NOIR,   // Poubelle pour les autres déchets
    JAUNE   // Poubelle pour les plastiques, cartons, canettes, conserves
}
